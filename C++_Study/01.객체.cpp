// 객체란, 변수들과 참고 자료들로 이루어진 소프트웨어 덩어리

// 추상화(abstraction) -> 객체가 현실 세계에서의 존재하는 것들을 표현

// 객체는 자기 만의 정보를 나타내는 변수들과, 이를 가지고 어떠한 작업을 하는 함수들로 둘러싸고 있다
// 이러한 객체의 변수나 함수들을 보통 인스턴스 변수(instance variable) 와 인스턴스 메소드(instance method)라 부른다

// 캡슐화(Encapsulation) -> 외부에서 직접 인스턴스 변수의 값을 바꿀 수 없고 항상 인스턴스 메소드를 통해서 간접적으로 조절하는 것